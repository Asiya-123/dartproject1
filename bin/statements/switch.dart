void main()
{
  int size=35;
  switch(size) {   // case must be similar to type of variable or expression

    case 30 :
      print("Size is 30");
      break; // to exit from particular case
    case 31 :
      print("Size is 31");
      break;
    case 32 :
      print("Size is 32");
      break;
    case 33 :
      print("Size is 33");
      break;
    case 34 :
      print("Size is 34");
      break;
    case 35 :
      print("Size is 35");
      break;
    default :
      print("Size not match");
  }
}